int Function(int) parcialSum(int numberOne) {
  int numberThree = 1;
  return (numberTwo) {
    return numberOne + numberTwo + numberThree;
  };
}

main() {
  print('Sum: 2 + 3 = ${parcialSum(2)(3)}');
  print('Sum: 2 + 7 = ${parcialSum(2)(7)}');
  print('Sum: 2 + 10 = ${parcialSum(2)(10)}');
  print('---');

  var finalSum = parcialSum(2);
  print('Sum: 2 + 3 = ${finalSum(3)}');
  print('Sum: 2 + 7 = ${finalSum(7)}');
  print('Sum: 2 + 10 = ${finalSum(10)}');
}
