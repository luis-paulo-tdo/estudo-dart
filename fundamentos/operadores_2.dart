main() {
  // Operadores de Atribução
  double numberOne = 2;
  numberOne += 3;
  numberOne -= 3;
  numberOne *= 3;
  numberOne /= 3;
  numberOne %= 3;
  print(numberOne);

  // Operadores Relacionais
  print(3 > 2);
  print(3 < 2);
  print(3 >= 2);
  print(3 <= 2);
  print(3 != 2);
  print(3 == 2);
  print(3 == '3');

  // Expressões com Operadores Aritméticos, Lógicos e Relacionais
  print(3 + 5 > 3 - 1 && 4 + 7 != 7 - 4);

  // Operadores Lógicos Bit a Bit
  print(5 & 4);
  print(5 | 4);
  print(5 ^ 4);
}
